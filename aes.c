//La transformation SubBytes
void SubBytes(STATE state)
{
	for (int i = 0; i < 16; i++)
		state[i] = sBox[state[i]];
}

//La transformation inverse de SubBytes
void invSubBytes(STATE state)
{
	for (int i = 0; i < 16; i++)
		state[i] = inv_sBox[state[i]];
}

//La transformation shift_rows
void ShiftRows(STATE state)
{
	STATE temp;
	for (int i = 0; i < 16; i++)
		temp[i] = state[i];
	for (int i = 0; i < 16; i++)
		state[i] = temp[shift[i]];
}

//La transformation inverse de ShiftRows
void invShiftRows(STATE state)
{
	STATE temp;
	for (int i = 0; i < 16; i++)
		temp[i] = state[i];
	for (int i = 0; i < 16; i++)
		state[i] = temp[invShift[i]];
}

//Rotation pour KeyExpansion
unsigned int Rot(uint32_t key)
{
	uint32_t temp = key >> (3 * 8);
	key = key << 8;
	key = key | temp;
	return (key);
}

//La subsitution urilisée dans la KeyExpanssion
unsigned int SubByteWord(uint32_t word)
{
	uint32_t ret = 0;
	for (int i = 0; i < 4; i++)
	{
		ret = ret << 8;
		ret = ret | sBox[(word >> (3-i)*8) & 0xFF];
	}
	return (ret);
}

//La fonction addRoundKey entre deux states
void addRoundKey(STATE s, STATE key)
{
	for (int i = 0; i < 16; i++)
		s[i] ^= key[i];
}

//Cette fonction prend un tableau d'octets de longueur 4 et le convertit en un entier non signé de 32 bits
uint32_t byte2word(uint8_t byte[])
{
	uint32_t resultat = 0;
	for (int i = 0; i < 4; i++)
	{
		resultat = resultat << 8;
		resultat = resultat | byte[i];
	}
	return (resultat);
}

//La fonction KeyExpanssion 
void key_expanssion(unsigned char key[], unsigned int word[], char Nk)
{
	unsigned int temp;
	for (int i = 0; i < Nk; i++)
		word[i] = byte2word(&key[4*i]);;
	for (int i = Nk; i < 4 * (Nk + 7); i++)
	{
		temp = word[i - 1];
		if (i % Nk == 0)
			temp = SubByteWord(Rot(temp)) ^ (rcon[i / Nk] << (24));
		else if ((i % Nk == 4) && (Nk == 8)) //Ce cas est pour la clé de 256 bits 
			temp = SubByteWord(temp);
		word[i] = word[i - Nk] ^ temp;
	}
}

//Cette fonction prend un entier non signé de 32 bits et le covertit à un tableau d'octets de longueur 4 
void word2key(uint32_t word[], STATE keys[], uint8_t Nk)
{
	for (int j = 0; j < Nk; j++)
	{
		for (int i = 0; i < 16; i++)
		keys[j][i] = (word[i/4 + j*4] >> ((3 - (i%4)) * 8)) & 0xFF;
	}
}

//Multiplication dans GF(2^8)
uint8_t mult_byte(uint8_t a, uint8_t b)
{
	uint8_t r = 0;
	while (b){
		if (b & 1) r ^= a;
		if (a & 0x80) a = (a << 1) ^ 0x1b;
		else a <<= 1;
		b >>= 1;
		}
	return r;
}

//La transformation mixed_column
void mixed_column(STATE s)
{
	STATE temp;
	for (int i = 0; i<16; i++) temp[i] = s[i];

	for (int c = 0; c<4; c++){
		for (int i=0; i<4; i++){
			s[i +4*c] = 0x00;
			for (int j=0; j<4; j++) 
				s[i +4*c] ^= mult_byte(temp[j +4*c] , mix[i][j]);
			}
	}
}

//La transformation inverse de mixed_column
void inv_mixed_column(STATE s)
{
	STATE temp;
	for (int i = 0; i<16; i++) temp[i] = s[i];

	for (int c = 0; c<4; c++){
		for (int i=0; i<4; i++){
			s[i +4*c] = 0x00;
			for (int j=0; j<4; j++) 
				s[i +4*c] ^= mult_byte(temp[j +4*c] , inv_mix[i][j]);
			}
	}
}


//L'algorithm du chiffrement AES pour les 3 tailles de clé pour un state unique
void cipher(STATE state, STATE keySet[], uint8_t Nk)
{
	//Le premier round
	addRoundKey(state, keySet[0]);

	//11 rounds de SubBytes -> ShiftRow -> MixColumn -> AddRoundKey
	for (int i = 1; i < (Nk+6); i++)
	{
		SubBytes(state);
		ShiftRows(state);
		mixed_column(state);
		addRoundKey(state, keySet[i]);
	}

	//Round final sans le Mixed Column
	SubBytes(state);
	ShiftRows(state);
	addRoundKey(state, keySet[Nk+6]);
}


//L'algorithm du dechiffrement AES pour les 3 tailles de clé pour un state unique
void inv_cipher(STATE state, STATE keySet[], uint8_t Nk)
{
   addRoundKey(state, keySet[Nk+6]);

   for (int i = Nk+5; i > 0; i--)
    {
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, keySet[i]);
        inv_mixed_column(state);
    }
   invShiftRows(state);
   invSubBytes(state);
   addRoundKey(state, keySet[0]);

}